#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *ptr;

	ptr = (unsigned char*)b;
	c = (unsigned char)c;
	while (len--)
	{
		*ptr = c;
		ptr++;
	}
	return (b);
}
